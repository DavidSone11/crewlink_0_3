package com.mathologic.projects.crewlink.custom.models;

public class TrainTypeVM {

	private Long id;
	private String name;
	
	public TrainTypeVM(Long id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
